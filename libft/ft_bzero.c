#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned char	*str;
	unsigned int	i;

	i = 0;
	str = (unsigned char *)s;
	while (s && i < n)
	{
		str[i] = '\0';
		s++;
		i++;
	}
	return (s);
}
